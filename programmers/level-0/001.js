//두 수의 합
function solution(num1, num2) {
  const answer = num1 + num2;
  return answer;
}

// 두 수의 차
function solution(num1, num2) {
  const answer = num1 - num2;
  return answer;
}

// 두 수의 곱
function solution(num1, num2) {
  const answer = num1 * num2;
  return answer;
}

// 몫 구하기
function solution(num1, num2) {
  const answer = parseInt(num1 / num2);
  return answer;
}
